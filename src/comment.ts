import type { GitHub } from '@actions/github/lib/utils';
import type { ActionInputs, AuditResult, Finding, Severity } from './types';
import { severityCounts } from './severity';

type Octokit = InstanceType<typeof GitHub>;

export const COMMENT_MARKER = '<!-- claim-auditor -->';

export async function postOrUpdateComment(
  octokit: Octokit,
  owner: string,
  repo: string,
  prNumber: number,
  body: string,
): Promise<void> {
  const fullBody = `${COMMENT_MARKER}\n${body}`;

  // Find an existing claim-auditor comment by marker. Paginate to handle PRs with
  // hundreds of comments — the bot might not be on page 1.
  let existingId: number | null = null;
  for await (const response of octokit.paginate.iterator(octokit.rest.issues.listComments, {
    owner,
    repo,
    issue_number: prNumber,
    per_page: 100,
  })) {
    for (const c of response.data) {
      if (c.body && c.body.startsWith(COMMENT_MARKER)) {
        existingId = c.id;
        break;
      }
    }
    if (existingId) break;
  }

  if (existingId !== null) {
    await octokit.rest.issues.updateComment({
      owner,
      repo,
      comment_id: existingId,
      body: fullBody,
    });
  } else {
    await octokit.rest.issues.createComment({
      owner,
      repo,
      issue_number: prNumber,
      body: fullBody,
    });
  }
}

export function formatNoMatchComment(glob: string): string {
  return [
    '## Claim Auditor — no reports to audit',
    '',
    `No files in this PR matched the configured glob \`${glob}\`. Audit skipped.`,
    '',
    `_Configure \`report_glob\` in your workflow to change which files are audited._`,
  ].join('\n');
}

export function formatComment(
  results: AuditResult[],
  filteredFindings: Finding[],
  inputs: ActionInputs,
): string {
  const counts = severityCounts(filteredFindings);
  const decisionSafe = counts.total === 0;

  const headline = decisionSafe
    ? `**Decision-safe.** No findings at or above ${inputs.severityFloor}.`
    : `**${counts.total} finding${counts.total === 1 ? '' : 's'}** at or above ${inputs.severityFloor}` +
      ` — ${counts.p1} P1 / ${counts.p2} P2 / ${counts.p3} P3.`;

  const lines: string[] = [
    `## Claim Auditor`,
    '',
    headline,
    '',
    `Audited ${results.length} file${results.length === 1 ? '' : 's'} with model \`${inputs.model}\`.`,
    '',
  ];

  if (counts.p1 > 0 && inputs.failOnP1) {
    lines.push(
      `> Check status will FAIL because \`fail_on_p1\` is enabled and ${counts.p1} P1 finding${counts.p1 === 1 ? ' was' : 's were'} found.`,
    );
    lines.push('');
  }

  // Per-severity sections (only those present at the floor).
  for (const sev of ['P1', 'P2', 'P3'] as Severity[]) {
    const findings = filteredFindings.filter((f) => f.severity === sev);
    if (findings.length === 0) continue;
    lines.push(`### ${sev} — ${severityLabel(sev)}`, '');
    lines.push(renderFindingsTable(findings));
    lines.push('');
  }

  // Per-file detail (collapsed) — surfaces skipped files + raw findings count.
  lines.push('<details>');
  lines.push('<summary>Per-file detail</summary>');
  lines.push('');
  lines.push('| File | Status | Raw findings |');
  lines.push('|---|---|---|');
  for (const r of results) {
    const status = r.skippedReason ? `skipped (${r.skippedReason})` : 'audited';
    lines.push(`| \`${r.file}\` | ${status} | ${r.findings.length} |`);
  }
  lines.push('');
  lines.push('</details>');
  lines.push('');
  lines.push(
    `_Generated by [claim-auditor-action](https://github.com/LaterKidsXD/claude-code-audit-action). ` +
      `Adversarial quantitative claim audit — catches probability-stacking, conditional-vs-marginal, and percentage-vs-pp errors._`,
  );

  return lines.join('\n');
}

function severityLabel(sev: Severity): string {
  switch (sev) {
    case 'P1':
      return 'decision-shaping, must address';
    case 'P2':
      return 'misleading framing';
    case 'P3':
      return 'pedantic';
  }
}

function renderFindingsTable(findings: Finding[]): string {
  const header = ['File', 'Quote', 'Issue', 'Correction'];
  const rows = findings.map((f) => [
    `\`${f.file}\``,
    escapeCell(f.quote),
    escapeCell(f.issue),
    escapeCell(f.correction ?? ''),
  ]);
  const lines = [
    `| ${header.join(' | ')} |`,
    `| ${header.map(() => '---').join(' | ')} |`,
    ...rows.map((r) => `| ${r.join(' | ')} |`),
  ];
  return lines.join('\n');
}

function escapeCell(text: string): string {
  // Replace pipes and newlines so they don't break the Markdown table layout.
  return text.replace(/\|/g, '\\|').replace(/\r?\n/g, ' ').trim();
}
